
package Kendaraan;

public class Vehicle {
private String noPol;
private String merk;
private int tahun;

public String getnoPol(){
    return noPol;
}
public void setnoPol(String noPol){
    this.noPol = noPol;
}
public String getmerk(){
    return merk;
}
public void setmerk(String merk){
    this.merk = merk;
}
public int gettahun(){
    return tahun;
}
public void settahun(int tahun){
    this.tahun = tahun;
}
    
}
